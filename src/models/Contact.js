import mongoose from "mongoose";

const contactSchema = mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      trim: true,
    },

    email: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

//making  model
const Contact = mongoose.model("contact", contactSchema);
export default Contact;
